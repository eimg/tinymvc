    <h2>About TinyMVC</h2>
    <p>
        TinyMVC is a PHP micro-framework designed for simplicity and speed. It provides a clean, modern, and object-oriented foundation for building web applications without unnecessary complexity.
    </p>
    <p>
        The framework has been modernized to use PHP 8+, with a secure public-facing entry point, Composer for autoloading, and a straightforward MVC architecture. It's perfect for small to medium-sized projects where performance and ease of use are top priorities.
    </p>
    <h3>Key Features:</h3>
    <ul>
        <li>Modern, object-oriented PHP 8+ architecture</li>
        <li>Secure design with a public web root</li>
        <li>Simple, file-based routing</li>
        <li>Basic MVC pattern for clean code organization</li>
        <li>Zero external dependencies</li>
    </ul>
